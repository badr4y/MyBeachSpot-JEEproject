package com.example.mybeachspot;

public class BeachInfo {

    private int id;
    private Beach beach;
    private String weather;
    private float waveLength;

    public int getId() {
        return id;
    }

    public Beach getBeach() {
        return beach;
    }

    public String getWeather() {
        return weather;
    }

    public float getWaveLength() {
        return waveLength;
    }
}
